fsm_status_t fsm_is_init(p_fsm_t * p_fsm_inst, bool * const p_is_init)
{
	fsm_status_t status = eFSM_OK;

	if ( NULL != p_is_init )
	{
		*p_is_init = gb_is_init;
	}
	else
	{
		status = eFSM_ERROR;
	}

	return status;
}




